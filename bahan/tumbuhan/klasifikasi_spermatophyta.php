                <p><b>Reproduksi Gymnospermae</b></p>
                <p>Dimulai dengan fase aseksual yaitu pembentukan sporofit difloid berupa sporofit
                    betina dan sporofit jantan, kerucut sporofit jantan mengandung mikrosporangia yang
                    kemudian yang membelah secara meosis menghasilkan gametofit jantan dan berkembang
                    menjadi serbuk sari jantan. Serbuk sari akan menghasilkan spermatozoid, sporofit betina
                    mengandung megasporangia yang akan membelah membentuk gametofit betina, pada
                    pembelahan meosis tersebut menghasilkan 4 megasporahaploid, dengan bantuan angin
                    terjadilah penyerbukan yang membawa serbuk sari jantan kea rah gametofit betina dan masuk
                    melalui celah mikrofil pada bakal biji gametofit betina. Sel sperma serbuk sari kemudian
