            <p>Gymnospermae disebut tumbuhan biji terbuka karena bakal bijinya tidak dilindungi
                oleh daun akan daging buah, namun biji berada di permukaan luar daun buah yang tersusun
                dalam strobilus, strobilus inilah yang menjadi alat perkembangbiakan tumbuhan
                Gymnospermae.</p>
            <p>Tumbuhan Gymnospermae di kelompokkan dalam 4 kelas :</p>
            <ol type="1">
                <li>Cycadinae (golongan pakis), bentuk tubuhnya menyerupai kelapa dan sering disebut
                    sebagai kelompok pakis, batang berbentuk tiang dan jarang bercabang, daun tersusun
                    dalam roset batang, daun menjemuk menyirip dan daun mudanya menggulung seperti
                    paku, sporofil tersusun atas strobilus jantan dan betina, pada umumnya berumah satu
                    artinya dalam satu pohon terdapat Bunga jantan dan bunga betina, tetapi ada juga berumah
                    dua dalam satu pohon hanya memiliki bunga jantan atau bunga betina saja. Contoh Cycas
                    rumpi dan Zarnia floridana.</li>
                <li>Cynkgoinae (tumbuhan raksasa), merupakan satu-satunya kelompok Gymnospermae
                    yang mempunyai spesies paling sedikit sehingga dianggap tumbuhan primitif, ciri-cirinya
                    lainnya yaitu tinggi pohonnya mencapai 30m dan termasuk pohon meranggas, daun lebar
                    seperti kipas dengan tulang daun mirip rusuk yang mononjol, tumbuhan ini kehilangan
                    daunnya pada musim gugur dan musim salju, kemudian bersemi kembali pada musim
                    semi, merupakan jenis tumbuhan berumah dua dan rangkaian sporofil terdapat pada tunas
                    pendek dalam ketiak daun. Contoh Ginkgo biloba.</li>
                <li>Gnetinae (kelompok melinjo), tumbuhan ini ada memiliki percabangan banyak dan ada
                    yang tidak bercabang, berumah dua tulang daunnya menyirip dan bunganya tersusun
                    berkarang, gnetinae lebih maju dibandingkan dengan kelas-kelas lain karena mempunyai
                    perhiasan pada strobilus, dalam perkembangannya perhiasan tersebut akan menjadi
                    mantel yang menyelubungi biji. Contoh Gnetum gnemon dan Ephedraaltissima.</li>
                <li>Coniferinae (kelompok golongan berdaun jarum), memiliki ciri-ciri batangnya besar
                    berkayu, daun berbentuk jarum, ada yang berumah satu dan ada yang berumah dua,
                    strobilus terletak diketiak daun atau diujung batang berupa kerucut sehinggu disebut
                    tumbuhan berkerucut, strobilus betinanya lebih besar dibandingkan strobilus jantan,
