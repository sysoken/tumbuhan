            <p>Untuk memperbanyak dirinya tumbuhan bermetagenisis secara gametofit dan
                sporofit, siklus hidup paku di awali dengan spora kemudian tumbuh menjadi protalium,
                protalium akan menghasilkan gamet jantan atau anteredium dan gamet betina atau
                arkegonium, anteredium menghasilkan spermatozoid sedangkan arkegonium menghasilkan
                sel telur atauovum, fase dari spora sampai ke spermatozoid dan sel telur disebut fase gametofit.
                Spermatozoid dan sel telur mengalami fertilisasi menghasilkan zigot, zigot membelah dan
                tumbuh menjadi tumbuhan paku, tumbuhan paku membentuk sporangium, fase dari zigot
                sampai ke sporangium disebut fase sporofit. Fase sporofit merupakan fase paling dominan
                dalam daur hidup paku, sporangium pecah menghasilkan spora dan siklus Kembali berulang.</p>
            <p>Selain menggunakan spora, paku juga bereproduksi secara vegetative melalui ubi
                batang, tunas pada tepi daun, tunas pada ujung daun, tunas akar, dan fragmentasi.</p>

