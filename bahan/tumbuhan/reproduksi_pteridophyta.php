    <h3> <img src="../gambar/microscope.png">
        <p class="textjudul">Ayo Mengamati</p>
    </h3>
    <div class="canvas">
        <p>Tumbuhan paku dapat melakukan reproduksi secara aseksual (vegetatif) dan seksual
            (generatif).
        </p>
        <ul>
            <li>Reproduksi aseksual dilakukan dengan pembentukan spora di dalam sporangium dan
                menggunakan rizom. Rizom akan membentuk tunas-tunas tumbuhan paku yang berkoloni.</li>
            <li>Reproduksi seksual dilakukan dengan pembentukan spermatozoid di dalam anteridium dan
                ovum di dalam arkegonium. Fertilisasi antara spermatozoid dan ovum akan menghasilkan
                zigot yang akan tumbuh menjadi sporofit atau tumbuhan paku.</li>
        </ul>
        <br>
        <p>Tumbuhan paku mengalami metagenesis antara generasi gametofit dan generasi sporofit.
            Secara umum, tahapan metagenesis pada tumbuhan paku adalah sebagai berikut :
        </p>
        <ul>
            <li>Spora paku haploid (n) yang jatuh di tempat lembap akan berkecambah dan berkembang
                menjadi protalium (gametofit) yang juga haploid (n).</li>
            <li>Protalium akan membentuk anteridium (n) dan arkegonium (n). Di dalam anteridium
                dibentuk spermatozoid (n), sedangkan di dalam arkegonium dibentuk ovum (n).</li>
            <li>Jika terjadi fertilisasi antara spermatozoid dan ovum, akan terbentuk zigot yang diploid
                (2n).</li>
            <li>Zigot akan tumbuh menjadi sporofit atau tumbuhan paku yang diploid (2n). Sporofit
                selanjutnya akan membentuk sporofil (daun pembentuk spora) yang juga diploid (2n).</li>
            <li>Sporofil (2n) akan membentuk sporangium (2n). Di dalam sporangium terdapat sel induk
                spora (2n) yang akan membelah secara meiosis membentuk spora haploid (n).</li>
        </ul>
        <br>
        <center>
            <img src="gambar/metagenesis-tumbuhan-paku.jpg" />
            <br>
            <i>Gambar 7. Metagenesis Tumbuhan Paku.</i>
        </center>
        <br>
        <br>
        <p>Berdasarkan jenis spora yang dihasilkan dikenal 3 jenis tumbuhan paku, yaitu:
        </p>
        <ul>
            <li>Paku Homospora (Isospora)</li>
            <p>Spora tipe homospora disebut juga dengan isospora. Disebut sebagai homospora
                karena bentuk dan ukuran sporanya sama. Sporanya akan berkecambah membentuk
                protalium, selanjutnya, di dalam satu protalium ini terdapat antheridium dan arkegonium.
                Karena antheridium dan arkegoniumnya ditemukan dalam satu protalium maka disebut juga
                berumah satu atau monoceous. Contoh tanaman paku yang besifat homospora yaitu paku
                kawat Lycopodium), suplir (Adiantum cuenatum), dan paku purba.</p>
            <br>
            <center>
                <img src="gambar/paku homospora.png" />
                <br>
                <i>Gambar 8. Metagenesis Homospora</i>
            </center>
            <li>Paku Heterospora (anisospore)</li>
            <p>Disebut heterospora karena memiliki spora yang berbeda bentuk dan ukurannya.
                Heterospora terdiri dari dua jenis spora yaitu mikrospora dan makrospora.</p>
            <p>Mikrospora yaitu spora yang berukuran kecil dan akan tumbuh menjadi
                mikroprotalium lalu akan menghasilkan antheridium sebagai penghasil gamet jantan.
                Sedangkan makrospora yaitu spora yang akan tumbuh membentuk makroprotalium dan akan
                menghasilkan arkegonium sebagai penghasil gamet betinanya. Contoh tanaman paku yang
                memiliki spora tipe heterospora yaitu semanggi (Marsilea crenata), dan paku rane (Selaginella
                sp.)</p>
            <br>
            <center>
                <img src="gambar/paku peralihan 1.jpg" />
                <br>
                <i>Gambar 9. Metagenesis Paku Heterospora.</i>
            </center>
            <li>Paku Peralihan</li>
            <p>Disebut sebagai peralihan karena memiliki spora dengan bentuk dan ukuran yang
                sama, tetapi dapat diketahui jenis kelaminnya. Karena, dari spora yang dihasilkan ada yang
                tumbuh membentuk protalium jantan dan menghasilkan spermatozoid dari antheridiumnya,
                ada juga yang tumbuh menjadi protalium betina menghasilkan ovum dari arkegoniumnya,
                padahal bentuk dan ukuran spora saat sebelum tumbuh sama. Contoh tumbuhan paku yang
                bertipe peralihan yaitu paku ekor kuda (Equisetum debile).</p>
            <br>
            <center>
                <img src="gambar/paku peralihan 1.jpg" />
                <br>
                <i>Gambar 10. Metagenesisi Paku Peralihan.</i>
            </center>
        </ul>
