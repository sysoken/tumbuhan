        <p>Tumbuhan paku diklasifikasikan berdasarkan ciri tubuhnya menjadi empat subdivisi, yaitu:
        </p>
        <ol type="a">
            <li>Paku Purba (Psilopsida)</li>
            <p>Dikatakan telanjang karena tidak berdaun atau daunnya kecil, ada pula yang tidak berakar
                sejati. Kebanyakan hidup di zaman purba dan ditemukan dalam bentuk fosil. Ada satu jenis
                yang sekarang masih ada tetapi hampir punah, yaitu Psilotum.</p>
            <p>Ciri – ciri Paku Purba:</p>
            <ul>
                <li>Struktur tubuhnya sederhana, dengan tinggi antara 30 cm – 1 m</li>
                <li>Umumnya tidak memiliki daun dan akar sejati, tetapi memiliki rizom yang dikelilingi
                    oleh rizoid. Jika terdapat daun, daunnya berukuran kecil (mikrofil) seperti sisik</li>
                <li>Batang beruas-ruas dan berbuku nyata, bercabang-cabang, berklorofil, serta memiliki
                    jaringan pengangkut • Sporangium terkumpul dalam sinangium yang terletak di
                    ketiak daun pada ruas-ruas batang</li>
                <li>Menghasilkan satu jenis spora (paku homospora).</li>
            </ul>
            <br>
            <center>
                <img src="gambar/paku purba.jpg" />
                <br>
                <i>Gambar 11. Psilotum nudum (paku purba).</i>
            </center>
            <br>
            <li>Paku Kawat (Lycopsida)</li>
            <p>Lycopsida memiliki ciri-ciri: berdaun kecil dan tersusun spiral, sporangium muncul di ketiak
                daun dan berkumpul membentuk strobilus (kerucut). Batangnya seperti kawat. Contohnya:
                Lycopodium, Selaginella, dan Isoetes.</p>
            <p>Lycopodiinae (paku kawat) adalah tumbuhan paku yang memiliki ciri-ciri sebagai berikut:</p>
            <ul>
                <li>Memiliki daun berbentuk seperti rambut atau sisik yang tersusun rapat pada batang</li>
                <li>Batang berbentuk seperti kawat. Pada bagian ujung batang terdapat sporangium yang
                    terkumpul dalam struktur seperti gada yang disebut strobilus</li>
                <li>Merupakan tumbuhan paku homospora atau heterospora</li>
                <li>Gametofit bersifat biseksual (menghasilkan dua jenis alat kelamin) atau uniseksual
                    (menghasilkan satu jenis alat kelamin)</li>
                <li>Banyak ditemukan hidup di hutan-hutan daerah tropis, baik tumbuh di permukaan
                    tanah atau sebagai epifit.</li>
            </ul>
            <br>
            <center>
                <img src="gambar/paku kawat.jpg" />
                <br>
                <i>Gambar 12. Lycopodium clavatum (Paku Kawat)</i>
            </center>
        </ol>
        <br>
        <li>Paku Ekor Kuda (Sphenopsida)</li>
        <p>Sphenopsida memiliki ciri-ciri: daun kecil, tunggal dan tersusun melingkar. Sporangium
            terdapat dalam strobilus (kerucut). Contohnya: Equisetum dan Calamites. Equisetinae (paku
            ekor kuda) adalah tumbuhan paku dengan percabangan batang yang khas seperti uliran atau
            lingkaran, sehingga menyerupai ekor kuda.</p>
        <p>Ciri-ciri dari anggota Equisetinae adalah sebagai berikut :</p>
        <ul>
            <li>Memiliki tubuh dengan tinggi rata-rata 1 m, tetapi ada juga yang tingginya mencapai
                4,5 m</li>
            <li>Sporofit berdaun kecil (mikrofil) dan berbentuk seperti sisik dengan warna agak
                transparan</li>
            <li>Batang beruas-ruas dan berongga, serta memiliki rizom</li>
            <li>Menghasilkan spora yang sama bentuknya, tetapi berbeda jenisnya (ada yang jantan
                dan ada yang betina), sehingga disebut paku peralihan</li>
            <li>Gametofit berukuran kecil dan mengandung klorofil, sehingga dapat berfotosintesis.
                Gametofit jantan tumbuh dari spora jantan dan menghasilkan anteridium. Sementara
                gametofit betina tumbuh dari spora betina dan menghasilkan arkegonium.</li>
        </ul>
        <br>
        <center>
            <img src="gambar/paku ekor kuda.jpg" />
            <br>
            <i>Gambar 13. Equisetum sp. (paku ekor kuda)</i>
        </center>
        </ol>
        <br>
        <li>Paku Sejati (Filicinae)</li>
        <p>Pteropsida merupakan tumbuhan paku yang dapat dilihat di sekitar kita, yang umum disebut
            pakis.</p>
        <p>Ciri-cirinya :</p>
        <ul>
            <li>daunnya besar, daun muda menggulung, sporangium terdapat pada sporofil (daun
                penghasil spora).</li>
            <li>Batang terdapat di bawah tanah atau berupa rizom</li>
            <li>Sporangium tersusun dalam sorus yang terletak di permukaan bawah daun, dengan
                posisi di sepanjang tepi daun atau di dekat tulang daun. Sorus umumnya dilindungi
                oleh indusium. Pada paku yang hidup di air, sporangium terdapat di dalam badan buah
                yang disebut sporokarpium</li>
            <li>Gametofit memiliki klorofil, dengan ukuran bervariasi. Gametofit bersifat uniseksual
                atau biseksual Contohnya: paku sarang burung (Asplenium nidus), suplir (Adiantum
                cuneatum), semanggi (Marsilea crenata)</li>
        </ul>
        <br>
        <center>
            <img src="gambar/paku sejati.jpg" />
            <br>
            <i>Gambar 14. Paku sejati</i>
        </center>
        </ol>
