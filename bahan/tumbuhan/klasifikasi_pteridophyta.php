            <p>Berdasarkan morfologi tubuhnya tumbuhan paku dibedakan menjadi4 kelas :</p>
            <ul>
                <li>Psilophytinae (paku perba), paku purba merupakan jenis paku yang paling sederhana dan
                    Sebagian bersa telah punah, memiliki batang beruas, bercabang dan berbuku-buku, pada bagian batang
                    tumbuh daun kecil berbentuk sisik, sporangiumnya terletak dibuku-buku
                    cabang atau ketiak daun. Contoh pseudosporochnus krejcii dan Psilotum mudum.</li>
                <li>Lycopodinae (paku kawat), paku ini memiliki batang bercabang dan menggarpu, daunnya
                    kecil seperti sisik, tersebar dibatang tidak bertangkai dan bertulang daun satu, beberapa
                    jenis paku ini ada memiliki lidah-lidah daun atau ligula, sporangiumnya membentuk
                    strobilus di ujung batang dan cabang. Contoh lycopodium cernuum dan selaginella
                    convoluta</li>
                <li>Equisetinae (paku ekor kuda), paku ini umumnya berupa terna atau berbatang lunak dan
                    hidup ditempat lembab, bentuk batang berongga memiliki cabang berkarang pada bukubuku batang,
                    dauunya berbentuk sisik, sporangium berbentuk strobilus pada ujung
                    batang, protalium berwarna hijau dan berkembang di luar spora. Contoh Equisetum sp
                    dan Rhynia elegans</li>
                <li>Filicinae (paku sejati), paku ini hidup ditempat lembab dan basah, berdasarkan
                    lingkungan hidupnya dibedakan menjadi paku air, paku tanah, paku epifit, daun sempurna
                    berukuran besar. Duduk daun pada batang membentuk sayap dan daun mudanya tumbuh
                    menggulung. Spora berkumpul membentuk sorus terdapat di permukaan daun, ujung
                    daun, dan tepi daun. Contoh paku ekor merak, paku tanduk rusa, dan suplir.</li>
            </ul>

            <p>Berdasarkan jenis-jenis spora yang dihasilkan, tumbuhn paku dibedakan menjadi 3, yaitu :</p>
            <p>1. Paku homospora, menghasilkan satujenis spora. Contoh Lycopodium sp</p>
            <p>2. Paku heterospora, menghasilkan 2 jenis spora, yaitu makrospora untuk betina dan
                mikrospora untuk jantan. Contoh Selaginella sp.</p>
            <p>3. Paku peralihan, menghasilkan 2 spora yaitu spora jantan dan betina yang bentuk dan
                ukurannya sama. Contoh Equisetum sp.</p>
