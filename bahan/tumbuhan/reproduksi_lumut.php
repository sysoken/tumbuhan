        <p>Metagenesis Tumbuhan Lumut</p><br>
        <p>Tumbuhan lumut mengalami metagenesis antara generasi gametofit dan
            generasi sporofit. Tahapan metagenesis pada tumbuhan lumut adalah sebagai berikut :</p><br>
        <ul>
            <li>Spora haploid (n) yang jatuh di tempat lembap akan berkecambah menjadi
                protonema (n)</li>
            <li>Protonema akan berkembang menjadi gametofit (n). Gametofit adalah tumbuhan
                lumut itu sendiri. Gametofit akan menghasilkan anteridium (n) dan arkegonium (n).</li>
            <li>Anteridium menghasilkan gamet jantan dan arkegonium menghasilkan gamet
                betina.</li>
            <li>Fertilisasi antara gamet jantan dan gamet betina akan menghasilkan zigot diploid
                (2n). Zigot akan berkembang menjadi sporofit. Pada sporofit terdapat sporangium
                (kotak spora).</li>
            <li>Di dalam sporangium, terdapat sel-sel induk spora diploid (2n) yang akan
                mengalami pembelahan meiosis menjadi spora haploid (n)</li>
        </ul>
