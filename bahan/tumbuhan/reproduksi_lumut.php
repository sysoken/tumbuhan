    <h3> <img src="../gambar/microscope.png">
        <p class="textjudul">Ayo Mengamati</p>
    </h3>
    <div class="canvas">
        <p>Pada lumut terjadi reproduksi secara vegetatif dan generatif. Reproduksi vegetatif terjadi
            dengan pembentukan spora melalui pembelahan meiosis sel induk spora di dalam sporangium (kotak
            spora). Spora tersebut kemudian tumbuh menjadi gametofit
        </p>
        <p>Pada lumut hati, reproduksi secara vegetatif (asesual) juga dapat dilakukan dengan
            pembentukan gemmae cup (piala tunas) dan fragmentasi (pemutusan sebagian tubuhnya). Reproduksi
            generatif terjadi melalui fertilisasi ovum oleh spermatozoid yang menghasilkan zigot. Zigot tersebut
            akan tumbuh menjadi sporofit. Sporofit berumur pendek; sekitar 3 – 6 bulan.
        </p>
        <p>Reproduksi lumut terjadi secara bergantian antara generatif dengan vegetatifnya, reproduksi
            vegetatifnya dengan spora haploid yang dibentuk dalam sporofit, sedangkan reproduksi generatifnya
            dengan membentuk gamet – gamet, baik gamet jantan maupun gamet betina yang dibentuk dalam
            gametofit. Ada 2 macam gametangium, yaitu sebagai berikut:
        </p>
        <ul>
            <li>Arkegonium adalah gametangium betina yang bentuknya seperti botol dengan bagian lebar
                yang disebut perut, bagian yang sempit disebut leher</li>
            <li>Anteredium adalah gametangium jantan yang berbentuk bulat seperti gada. Dinding anteredium
                terdiri dari selapis sel yang mandul dan didalamnya terdapat sejumlah sel induk spermatozoid.</li>
            <li>Tumbuhan lumut yang menghasilkan dua macam alat kelamin pada tubuh yang sama disebut
                lumut berumah satu (monoesis), sedangkan tumbuhan lumut yang menghasilkan alat kelamin
                pada tubuh yang berbeda disebut lumut berumah dua (diesiss). Pada lumut berumah dua,
                tumbuhan yang menghasilkan anteridium disebut gametofit jantan dan tumbuhan yang
                menghasilkan arkegonium disebut gametofit betina</li>
        </ul>
        <br>
