        <div class="kesimpulan">

            <p>Reproduksi tumbuhan lumut mengalami pergiliran keturunan atau metagenesis dari
                generasi gametofit dan generasi sporofit</p>
            <p>Di dalam siklus hidupnya, lumut akan mengalami pergiliran keturunan (metagenesis)
                antara generasi gametofit yang berkromosom haploid (n) dengan generasi sporofit yang
                berkromosom diploid (2n).</p>
            <p>Tumbuhan Angiospermae, Gymnospermae, dan tumbuhan paku dapat dijumpai pada
                tahap sporofit. Akan tetapi, tumbuhan lumut yang sering kita jumpai berada pada tahap
                gametofit.</p>
            <p>Reproduksi seksual (generatif) tumbuhan lumut dimulai ketika spora berkecambah
                menghasilkan protonema. Protonema kemudian tumbuh menjadi tumbuhan lumut</p>
            <p>Dari ujung batang berkembang organ reproduksi betina (arkegonium) dan organ
                reproduksi jantan (anteridium)</p>
            <p>Arkegonium menghasilkan sel telur atau ovum, sedangkan anteridium menghasilkan
                sperma yang berflagela dua</p>
            <p>Sperma kemudian berenang untuk membuahi sel telur. Pembuahan akan dapat
                berlangsung dengan baik apabila lingkungannya basah dan berair. Hasil pembuahan tersebut
                membentuk zigot.</p>
            <p>Zigot selanjutnya tumbuh menjadi sporofit yang bersifat haploid (n). Pada saat sporofit
                masak (umur 3 – 6 bulan) akan membentuk tangkai panjang (seta)</p>
            <p>Ujung seta berupa kapsul yang disebut dengan sporogonium. Di dalam sporogonium
                terdapat spora.</p>
            <p>Ketika spora telah masak, kapsul pelindungnya akan pecah, sehingga spora dapat
                dibebaskan. Spora yang dibebaskan tersebut akan berkecambah dan memulai siklus hidup
                lumut Kembali</p>

