        <ul>
            <li>Spermatophyta berasal dari bahasa yunani, yaitu sperma yang berarti biji, dan phyton yang
                berarti tumbuhan. Meliputi semua tumbuhan berpembuluh yang bereproduksi secara generatif
                dengan membentuk biji. Di dalam biji terdapat calon individu baru (embrio sporofit atau
                lembaga) beserta cadangan makanan yang terbungkus oleh lapisan pelindung. Spermatophyta
                merupakan anggota plantae sejati dan menghasilkan biji untuk perkembangbiakannya
                (kormofita berbiji) sedang alat perkembangbiakannya tampak jelas dapat diamati sehingga
                disebut sebagai Phanerogamae. Tumbuhan berbiji meliputi semua tumbuhan yang
                menghasilkan biji.</li>
            <li>Perkembangbiakan tumbuhan biji terjadi secara generatif (seksual) dengan membentuk biji
                yang diawali dengan pembentukan gamet (gametogenesis), penyerbukan (polinasi), peleburan
                gamet jantan dan betina (fertilisasi) yang menghasilkan Misal, kemudian menjadi embrio.
                Perkembangan secara vegetatif (aseksual) dengan organ-organ vegetatif seperti tunas, tunas
                adventif, rhizoma, dan stolon.</li>
            <li>Gymnospermae bereproduksi secara generatif (seksual) dengan membentuk biji. Alat
                reproduksinya berupa strobilus terbentuk ketika tumbuhan sudah dewasa. Gymnospermae
                mengalami pembuahan tunggal.</li>
            <li>Angiospermae memiliki alat perkembangbiakan yaitu berupa bunga. Reproduksi pada
                Angiospermae diawali dengan adanya proses penyerbukan (menempelnya serbuk sari pada
                kepala putik) dan proses pembuahan (penyerbukan sel telur dan kantong lembaga pada bakal
                biji dengan inti yang berasal dari serbuk sari). Selanjutnya zigot berkembang menjadi embrio
                dan kemudian menjadi buah. Pembuahan yang terjadi pada Angiospermae disebut pembuahan
                ganda, karena dua inti generatif (sperma) masing-masing membuahi sel telur yang akan menjadi
                lembaga dan inti kandung lembaga menjadi endosperm Tumbuhan ini memiliki arti penting bagi organisme
                lain di bumi. Bahan makanan manusia dan hewan banyak yang berasal dari
                tumbuhan berbiji.</li>
