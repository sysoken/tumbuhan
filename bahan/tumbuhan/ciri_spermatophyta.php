            <p>Spermatophyte atau tumbuhan biji merupakan tumbuhan yang menghasilkan biji sebagai
                alat perkembangbiakan, tuubhnya bercormosphyta, memiliki alat kelamin yang jelas dan
                menghasilkan embrio, cara hidupnya dengan fotoautotrof atau menghasilkan makanan sendiri
                dengan bantuan cahaya matahari, memiliki berkas pembuluh pada bagian tubuhnya dan ada yang
                hidup di darat juga di air.</p>
            <p>Sebagai tubuh bercormos struktur tubuh spermatophyta terdiri dari akar, batang, daun, bunga,
                dan buah.</p>
            <ul>
                <li>Akar spermatophya ada yang berakar tunggang dan ada akar serabut. Sel akarnya sudah
                    mengalami diferensiasi menjadi epidermis korteks, serta silinder pusat yang didalanya
                    terdapat xylem dan floem.</li>
                <li>Batang spermatophyta ada yang merayap, condong, tegak, dan berbaring. Sel batangnya
                    juga sudah mengalami diferensiasi menjadi epidermis, korteks, dan silinder pusat serta
                    xylem dan floem.</li>
                <li>Sedangkan bagian daunnya memilki tulang daun dengan beragam bentuk dan ukuran, sel
                    daun sudah mengalami deferensiasi menjadi epidermis dan mesofil, mesofil tersusun atas
                    jaringan tiang dan jaringan bunga karang.</li>
            </ul>

            <br>
            <p>Berdasarkan letak bakal biji atau bijinya spermatophyte dikelompokkan menjadi dua :</p>
            <ol type="1">
                <li>Gymnospermae (tumbuhan berbiji terbuka), yaitu bakal biji tidak dilindungi oleh daun
                    buah (karpel) atau bijinya berad pada bilah-bilah strobilus berbentuk sisik.</li>
                <li>Angiospermae (tumbuhan berbiji tertutup), yaitu bakal biji dilindungi oleh daun buah
                    atau karpel</li>

            </ol>
