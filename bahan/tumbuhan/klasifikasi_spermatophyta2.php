            <p>Tumbuhan ini disebut sebagai tumbuhan berbiji tertutup karena bakal bijinya tumbuh di
                dalam daging buah, biji berada di dalam ruang buah dan menempel pada daun buah. Selain itu
                angiospermae juga disebut dengan anthophyta atau tumbuhan berbunga karena tumbuhan ini
                berkembangbiak dengan bunga</p>
            <p><b>Karakteristik tumbuhan Angiospermae di kelompokkan menjadi 2 kelas :</b></p>
            <ol type='1'>
                <li>Monocotyledonae (tumbuhan berbiji tunggal), Tumbuhan monokotil memiliki akar serabut,
                    memiliki pelindung berupa tudung dan akar Lembaga, memliki biji berkeping satu artinya
                    tidak membelah saat berkecambah. Akar dan batang tumbuhan monokotil tidak berkambium, tulang daun
                    moonokotil berbentuk sejajar atau melengkung, jumlah kelopak
                    bunga pada tumbuhan monokotil adalah 3 dan kelipatannya. <br>
                    <p><b>Klasifikasi monocotyledoneae:</b></p>
                    <ul>
                        <li>Gramineae atau rumput-rumputan. Contoh padi, jagung, dan alang-alang.</li>
                        <li>Palmae atau pinang-piangan. Contoh kelapa, sagu, dan palem.</li>
                        <li>Musaceae atau pisang-pisangan. Contoh pisang ambon dan pisang raja.</li>
                        <li>Orchidaceae atau anggrek. Contoh anggrek dan fanili</li>
                        <li>Zingiberaceae atau jahe-jahean. Contoh jahe, kunyit, dan lengkuas.</li>
                        <li>Liliaceae. Contoh kembang sungsang.</li>
                    </ul>
                </li>
                <li>Dicotyledoneae (tumbuhan berbiji ganda), Tumbuhan dikotil berakar tunggang, tidak
                    bertudung dan tidak memiliki akar lembaga, memiliki biji berkeping dua. Akar dan batan
                    dikotil berkambium sehingga dapat tumbuh membesar, tumbuhan dikotil bertulang menjari
                    atau menyirip, jumlah kelopak bunga pada dikotil berkelipatan 4 atau lima. <br>
                    <p><b>Klasifikasi dicotyledoneae :</b></p>
                    <ul>
                        <li>Papilionaceae atau polong-polongan. Contoh kacang tanah.</li>
                        <li>Euphorpisaceae atau jarak-jarakan. Contoh tanama jarak dan karet.</li>
                        <li>Solonaceae atau terong-terongan. Contoh terong, cabe, dan tomat.</li>
                        <li>Myrtaceae atau jambu-jambuan. Contoh jambu biji dan jambu air.</li>
                        <li>Compositae atau komposit. Contoh bunga matahari.</li>
                        <li>Convolvulaceae. Contoh kangkung, ubi jalar, dan bunga terompet.</li>
                        <li>Apocynaceae. Contoh kamboja dan tapak darah.</li>
                        <li>Rubiaceae. Contoh kopi dan mengkudu.</li>
                        <li>Verbenaceae. Contoh jati dan bongsai.</li>
                        <li>Ruraceae. Contoh jeruk</li>
                        <li>Bombacaceae. Contoh durian</li>
                        <li>Mimosaceae. Contoh putri malu</li>
                    </ul>

                </li>
            </ol>

            <p><b>Reproduksi Angiospermae :</b></p>
            <p>Angiospermae bereproduksi secara generatif dan vegetatif, proses pembuahannya
                dikenal dengan pembuahan ganda, diawali dengan penyerbukan atau polinasi yaitu
                penempelan butir serbuk sari kekepala putik. Sel kelamin jantan pada bunga mengandung
                mikrospora yang membelah secara meosis menghasilkan gametofit jantan dan berkembang
                menjadi serbuk sari jantan, sel kelamin betina mengandung megaspora yang akan membelah
                membentuk gametofit betina, setelah itu butik serbuk sari berkecambah dikepala putik
                membentuk buluh serbuk sari, inti sel serbuk sari membelah menjadi sel vegetatif dan sel
                generatif</p>
            <p>Cecara generatif membelah secara metosis menghasilkan dua sel sperma, sel
                vegetatif bergerak ke buluh serbuk sari yang menuju bakal buah. Saat serbuk sari
                mencapai bakal biji inti vegetatif menembus kantong embrio melalui mikrofil dan
                melepaskan kedua sel sperma, satu sel sperma atau inti sel generatif satu membuahi sel
                telur membentuk sel zigod, sel sperma lainnya atau inti sel generatif dua membuahi dua
                inti kandung lembaga sekunder sehingga berbentuk sel diploid, sel ini akan membelah
                membentuk jaringan penyimpan cadangan makanan yang disebut endosperma.
                Endosperma menyediakan makanan bagi embrio yang berkembang dari zigod, dua
                pristiwa fusi yang terjadi anatara sel sperma dengan sel telur, dan sel sperma dengan inti
                kandung lembaga sekunder inilah yang dikenal dengan pembuahan ganda pada
                Angiospermae. Proses selanjutnya adalah perkembangan biji menjadi tumbuhan muda,
                kemudian tubuh menjadi Angiospermae dewasa dan siklus Kembali berulang.</p>
            <p><b>Peranan agiospermae :</b></p>
            <ol>
                <li>Serat tanaman kapas digunakan sebagai bahan pakaian.</li>
                <li>Kayu jati sebagai bahan bangunan dan perabotan rumah tangga.</li>
                <li>Lada dan cengkeh sebagai rempah-rempah.</li>
                <li>Eceng gondok sebagai pakan ternak.</li>
                <li>Sebagai bahan obat-obatan, seperti pinang untuk obat cacing.</li>
                <li>Lidah buaya menjadi obat luka dan radang kulit.</li>
                <li>Kelapa sawit, tebu, tembakau diolah untuk industry.</li>
                <li>Dan beragam buah-buahan yang dikonsumsi sebagai sumber vitamin.</li>
            </ol>

