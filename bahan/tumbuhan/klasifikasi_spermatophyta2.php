        <h4>2. Angiospermae (Berbiji tertutup)</h4>
        <p>Disebut biji tertutup karena bijinya terbungkus oleh daging buah. Memiliki alat reproduksi
            berupa bunga sempurna (benang sari, putik, bakal buah, bakal biji, mahkota, kelopak, dan
            tangkai). Reproduksi generatif mengalami dua kali pembuahan (pembuahan ganda) yang
            menghasilkan zygot (pembuahan inti generatif/sperma dengan ovum) dan endosperm
            (pembuahan inti generatif/sperma dengan kandung lembaga skunder). Pada umumnya
            tumbuhan ini berupa pohon, perdu, semak, liana, atau herba.</p><br>

        <center>
            <img src="gambar/Angiospermae.png" />
            <br>
            <i>Gambar 14. Bunga Angiospermae</i>
        </center>
        </ol>
        <p><b>Ciri-ciri Angiospermae</b></p>
        <ul>
            <li>Bakal biji diselubungi daun buah yang merupakan bakal buah.</li>
            <li>Berupa herba, perdu, atau pohon.</li>
            <li>Mempunyai organ yang berupa bunga lengkap (terdapat kelopak bunga, mahkota
                bunga, serta alat kelamin berupa benang sari dan putik).</li>
        </ul>

        <br>

        <p><b>Klasifikasi Angiospermae:</b></p>
        <p>Angiospermae dibagi menjadi 2 kelas, yaitu:</p>
        <ol type="a">
            <li><b>Kelas Monocotyledoneae</b></li>
            <p>Ciri-ciri kelas Monocotyledoneae adalah sebagai berikut :</p>
            <ol type="1">
                <li>Berbiji tunggal (hanya memiliki 1 daun lembaga), sistem akar serabut, batang
                    sama besar dan tidak bercabang.</li>
                <li>Daun tunggal berpelepah dan bertulang sejajar. Bagian bunga kelipatan tiga.</li>
                <li>Akar dan batang tidak berkambium, sehingga tidak dapat tumbuh membesar.</li>
                <li>Xilem dan floem tersebar. Contoh kelas monocotyledoneae adalah : Oryza
                    sativa(padi), Zea mays (jangung), dan Cocos nucifera (kelapa)</li>
            </ol>
            <li><b>Kelas Dicotyledoneae</b></li>
            <p>Ciri-ciri kelas dicotyledoneae adalah sebagai berikut :</p>
            <ol type="1">
                <li>Berkeping dua (memiliki dua daun lembaga), memiliki akar tunggang.</li>
                <li>Batang kerucut panjang, bercabang dan berkambium.</li>
                <li>Daun tunggal atau majemuk, tulang daun menyirip atau menjari.</li>
                <li>Bagian bunga kelipatan dua, empat atau lima.</li>
                <li>Memiliki kambium sehingga dapat mengalami pertumbuhan sekunder
                    (pertumbuhan melebar), xilem dan floem tersusun dalam lingkaran.
                    Contoh kelas dicotyledoneae: Mangifera indica (mangga),Manihot utilissima
                    (ketela pohon), dan Psidium guajava (jambu biji).</li>
            </ol>

