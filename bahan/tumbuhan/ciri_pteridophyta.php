    <h3>
        <p class="textjudul">&nbsp; Ciri-ciri Pteridophyta</p>
    </h3>
    <div class="canvas">
        <p>Tumbuhan paku merupakan salah satu kelompok tumbuhan yang tertua yang masih dapat
            dijumpai di daratan. Tumbuhan berkormus adalah tumbuhan yang memiliki batang, akar, dan daun
            yang sebenarnya. Artinya, batang, akar, dan daunnya sudah memiliki pembuluh angkut xilem dan
            floem
        </p>
        <br>
        <p>
            Ciri – ciri tumbuhan paku sebagai berikut :
        </p>
        <ol type='a'>
            <li>Sudah mempunyai akar, batang, dan daun yang jelas.</li>
            <li>Pada batang sudah terdapat jaringan pengangkut, dengan sistem konsentris.</li>
            <li>Terjadi metagenesis.</li>
            <li>Generasi sporofit mempunyai akar sejati, berumur panjang dan merupakan a. keturunan
                generatif.</li>
            <li>Generasi gametofitnya adalah protalium, tidak mempunyai akar sejati, serta a. mempunyai
                anteridium dan arkegonium.</li>
            <li>Embrionya berkutub satu</li>
            <li>Ujung daun paku yang muda umumnya menggulung.</li>
            <li>Akar paku berupa akar serabut, terdapat kaliptra, tipe pembuluh angkut a. konsentrik.</li>
            <li>Batang umumnya berupa akar tongkat, kecuali pada paku tiang dan sejenisnya.</li>
            <li>Daun paku dapat dibedakan menjadi mikrofil dan makrofil, tetapi dapat juga dibedakan
                menjadi sporofil dan tropofil.</li>
        </ol>
        <br>
        <p><b>Struktur Tumbuhan Paku</b></p>
        <p>Struktur tubuh paku memiliki 4 struktur penting, yaitu lapisan pelindung sel (jaket
            steril) yang terdapat disekeliling organ reproduksi, embrio multiseluler yang terdapat dalam
            arkegonium, kutikula pada bagian luar dan yang paling penting adalah sistem transport internal
            yang mengangkut air dan zat makanan dari dalam tanah. Sistem transport ini sama baiknya seperti
            pengorganisasian transport air dan zat makanan pada tumbuhan tingkat tinggi. Struktur
            Tubuh Tumbuhan Paku:</p>
        <ul>
            <li>Akar: Bersifat seperti akar serabut, ujungnya dilindungi kaliptra yang terdiri atas sel – sel
                yang dapat dibedakan dengan sel – sel akarnya sendiri.</li>
            <li>Batang: Pada sebagian jenis tumbuhan paku batang tidak tampak karena terdapat di dalam
                tanah berupa rimbang, mungkin menjalar atau sedikit tegak. Jika muncul di atas permukaan
                tanah, batangnya sangat pendek sekitar 0,5 m, akan tetapi ada batang beberapa jenis
                tumbuhan paku seperti paku pohon atau paku tiang yang panjangnya mencapai 5 m dan
                kadang – kadang bercabang misalnya: Alsophilla dan Cyathea.</li>
            <li>Daun: Bentuknya selalu melingkar dan menggulung pada usia muda. berdasarkan bentuk
                ukuran dan susunanya, daun paku dibedakan antara epidermis, daging daun, dan tulang
                daun. Berdasarkan bentuk daun dibedakan lagi menjadi mikrofil dan makrofil, berikut
                penjelasannya:</li>
            <li>Mikrofil: Daun ini berbentuk kecil – kecil seperti rambut atau sisik, tidak bertangkai dan
                tidak bertulang daun, belum memperlihatkan diferensiasi sel, dan tidak dapat dibedakan
                antara epidermis, daging daun dan tulang daun.</li>
            <li>Makrofil: Merupakan daun yang bentuknya besar, bertangkai dan bertulang daun, serta
                bercabang – cabang. Sel – sel penyusunnya telah memperlihatkan diferensiasi, yaitu dapat
                dibedakan antara jaringan tiang, jaringan bunga karang, tulang daun, serta stomata (mulut
                daun). Daun paku tumbuh dari percabangan tulang daun yang disebut frond, dan
                keseluruhan daun dalam satu tangkai daun disebut jika diperhatikan pada permukaan
                bagian daun (frond) terdapat bentuk berupa titik-titik hitam yang disebut sorus, dalam sorus
                terdapat kumpulan sporangia yang merupakan tempat atau wadah dari spora. Tidak semua
                daun paku memiliki sorus (sori), daun paku yang memiliki sorus merupakan daun fertil
                yang disebut daun sporofil, daun paku yang tidak memiliksorus disebut daun steril. Daun
                ini banyak mengandung klorofil dan banyakdimanfaatkan untuk proses fotosintesis. Daun
                ini disebut daun tropofil.</li>
        </ul>
