            <p>Teman-teman bisa mengamati tumbuhan paku disekita kita, biasanya tumbuhan paku memiki
                daun muda yang menggulung, hidup ditempat yang lembab, dan jika daunnya dibalik terdpat
                kumpulan sporangium berbentuk bulat kehitaman yang disebut sorus, berbedan dengan lumut
                tumbuhan paku sudah memiliki akar, batang dan daun sejati sehingga termasuk cormophyte. Pada
                proses metagenesisnya fase sporofit lebih dominan dari pada gametofit.</p>
            <br>
            <p>Bagian-bagian tumbuh paku, sebagai tumbuhan bercormos aku memiliki akar sejati berbentuk
                serabut, batang yang berada didalam tanah atau rizome dan daun, daun mudanya tumbuh
                menggulung dan juga terdapat tangkai daun, akar dan batang paku sudah memiliki tubuh.</p>
            <p>Daun pteridophyte memiliki berbagai jenis :</p>
            <p>Perbedaan fase gametofit dan fase sporofit :</p>
            <p>Berdasarkan ukuran :</p>
            <p>1. Mikrofil, ukurannya kecil, seperti sisik, tidakbertangkai, dan tidak bertulang daun</p>
            <p>2. Makrofil, ukurannya besar, bertulang daun, serta selnya telah mengalami diferensiasi.</p>
            <p>Berdasarkan fungsi :</p>
            <p>1. Tropofit, daun steril untuk fotosintesis</p>
            <p>2. Sporofil, daun fertile untuk menghasilkan spora.</p>
            <p>Spora pada paku tersusun dalam kotak sporangium. Berdasarkan susunan dan letaknya ada 4
                bentuk sporangium :</p>
            <p>1. Strobilus pada ujung batang.</p>
            <p>2. Sinangium pada ketiak daun.</p>
            <p>3. Sorus pada permukaan daun.</p>
            <p>4. Sporokarpium didalam badan buah.</p>
