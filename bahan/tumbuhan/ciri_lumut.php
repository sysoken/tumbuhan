    <div class="canvas">
        <p>
            Apakah kalian pernah mengamati alam di sekitar lingkungan kalian? Lumut banyak
            terdapat di tempat – tempat yang lembab. Mungkin kalian pernah melihat lumut tersebut di
            lingkungan kalian? Perhatikan gambar lumut tersebut yang menempel dibebatuan.
        </p>
        <center>
            <img src="gambar/images.jpeg" />
            <br>
            <i>Gambar 1. Lumut menempel di bebatuan.</i>
        </center>
        <p>Pembelajaran kita kali ini diawali dengan membahas tentang lumut yang merupakan salah satu
            kelompok tumbuhan pada Kingdom Plantae. Lumut (Bryophita) berasal dari bahasa Yunani yaitu bryon
            yang berarti “Tumbuhan Lumut“. Pada umumnya, lumut berwarna hijau karena mempunyai sel– sel
            yang memiliki pigmen hijau berupa klorofil sehingga lumut memiliki kemampuan untuk menghasilkan
            senyawa organik melalui proses fotosintesis yang terjadi di dalamnya. Itulah sebabnya lumut tergolong
            organisme fotoautotro.
        </p>
        <br>

        <p>Struktur Tumbuhan Lumut</p><br>
        <p>Struktur Tubuh Lumut bryophyta – Tubuhnya tidak memiliki akar, batang, dan
            daun yang sebenarnya, tetapi mempunyai bagian yang menyerupai akar (rizoid), batang, dan
            daun. Pada beberapa jenis lumut hati atau lumut tanduk tubuhnya masih
            berupa talus (lembaran).</p><br>
        <p>Rizoid adalah struktur menyerupai rambut atau benang-benang yang berfungsi untuk
            melekatkan tubuh pada tempat tumbuhnya dan menyerap air serta garam-garam mineral. Rizoid
            ini terdiri dari satu deret sel yang memanjang, terkadang dengan sekat yang tidak sempurna.
            Batang dan daun lumut belum memiliki floem maupun xylem. Sel-sel penyusun tubuhnya
            memiliki dinding sel yang terdiri dari selulose. Lumut tidak memiliki sistem pembuluh
            pengangkut yang khusus untuk mengangkut air dan mineral organik, sehingga proses
            pendistribusian air berjalan lambat yaitu secara difusi.</p><br>
        <p>Daun lumut umumnya disusun oleh sel-sel setebal 1 lapis, kecuali ibu tulang daun,
            yang mempunyai lebih dari 1 sel. Sel-selnya sempit, panjang, kecil, dan mengandung kloroplas
            yang tersusun seperti jala. Tubuh tumbuhan lumut dengan berbagai struktur umum tersebut
            adalah gametofit. Setelah dewasa, lumut akan membentuk sporofit. Sporofit adalah struktur
            tubuh lumut yang terdiri atas bagian-bagian tertentu, yaitu vaginula, kaliptra, dan kolumela.
            (lihat gambar utama postingan di atas)</p><br>
        <p>1) Vaginula adalah bagian sporofit yang terdiri dari kaki yang diselubungi sisa dinding
            arkegonium, seta (tangkai), dan apofisis yaitu ujung seta yang agak melebar, yang
            merupakan peralihan antara seta dengan kotak spora (sporangium)</p>
        <p>2) Kaliptra adalah tudung yang berasal dari dinding arkegonium sebelah atas menjadi tudung
            kotak spora.</p>
        <p>3) Sedangkan kolumela adalah jaringan yang tidak ikut mengambil bagian dalam
            pembentukan spora.</p>
        <p>Sporofit tumbuhan lumut tumbuh menumpang pada gametofit yang hijau menyerupai
            daun. Sporofit ini memiliki klorofil, sehingga dapat berfotosintesis. Namun, tumbuhan lumut
            juga bisa mendapatkan makanan dari gametofit tempatnya melekat.</p>
